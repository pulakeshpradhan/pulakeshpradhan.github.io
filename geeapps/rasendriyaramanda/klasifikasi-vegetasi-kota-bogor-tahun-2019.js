// Pilih band untuk keperluan training 
var bands = ['B2', 'B3', 'B4', 'B5', 'B6', 'B7','B8'];
// Sample input citra untuk mendapatkan "FeatureCollection" dari data training.
var training = image2.select(bands).sampleRegions({
  collection: newfc,
  properties: ['landcover'],
  scale: 30
});
//==========================================================================================
// Train the classifier
// Buat "RANDOM FOREST classifier dan train".
var classifier = ee.Classifier.randomForest().train({
  features: training,
  classProperty: 'landcover',
  inputProperties: bands
});
//==========================================================================================
// Klasifikasi citra dan tampilkan hasilnya
// Klasifikasi citra asupan.
var classified = image2.select(bands).classify(classifier);
// Definisikan palet untuk mewarnai klasifikasi.
var palette1 = [
  'D3D3D3', // umum (0)  // grey
  '008000', //  vegetasi (1) // green
];
// Tampilkan hasil klasifikasi dan citra asupan.
Map.addLayer(classified, {min: 0, max: 1, palette: palette1}, 'Hasil Klasifikasi');
//==========================================================================================
// Kaji akurasi perhitungan
// Gunakan "confusion matrix" untuk menunjukkan hasil akurasi.
print('RF error matrix: ', classifier.confusionMatrix());
print('RF accuracy: ', classifier.confusionMatrix().accuracy());
//==========================================================================================
//Menghitung Luasan Area yang Sudah Diklasifikasi
// Memisahkan hasil dalam 2 kelas klasifikasi
var classified2 = classified.reduce('sum').toInt();
//Menghitung luasan area Berdasarkan Pixel
var pixstats = classified2.reduceRegion({
  reducer: ee.Reducer.count(),                  // menghitung pixel di satu lapisan
  geometry: bogor,
  scale: 30,
  bestEffort:true,
  });
var allpixels = ee.Number(pixstats.get('sum')); // extract jumlah pixel menjadi angka
// Membuat daftar kosong untuk menyimpan suatu nilai area
var arealist = [];
print('Jumlah Pixel', allpixels);
// Menghitung luas wilayah klasifikasi
var areacount = function(cnr, name) {
 var singleMask =  classified2.updateMask(classified2.eq(cnr));  // menutupi satu kelas
 var stats = singleMask.reduceRegion({
  reducer: ee.Reducer.count(),               // menghitung jumlah pixel dalam satu kelas
  geometry: bogor,
  scale: 30,
  bestEffort:true,
  });
var pix =  ee.Number(stats.get('sum'));
var hect = pix.multiply(900).divide(10000);                // Landsat pixel = 30m x 30m --> 900 sqm
var perc = pix.divide(allpixels).multiply(10000).round().divide(100);   // membentuk presentase area berdasarkan kelas klasifikasi dan dalam bentuk desimal 2 dibelakang koma
arealist.push({Class: name, Pixels: pix, Hectares: hect, Percentage: perc});
};
// Jenis Klasifikasi
var names2 = ['Non Vegetasi','Vegetasi'];
// Menjalankan fungsi di setiap kelas
for (var i = 0; i < 2; i++) {
  areacount(i, names2[i]);
  }
print('Hasil Perhitungan Luas', arealist);
