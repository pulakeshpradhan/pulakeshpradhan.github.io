var getClassified = function(image)
{
  var bands = ['B2','B3','B4','B8','B11','B12'];
  var sample = newfc.randomColumn();
  var split = 0.6;
  var train = sample.filter(ee.Filter.lte('random', split));
  var validate = sample.filter(ee.Filter.gt('random', split));
  // print(train.size(),'training sample size');
  // print(validate.size(),'validation sample size');
  var training = image.select(bands).sampleRegions(
    {collection: train,
      properties:['landcover'],
      scale:10});
  var palette = ['red','yellow','darkgreen','pink','blue'];
  var classifier = ee.Classifier.smileRandomForest(10).train({
    features: training,
    classProperty: 'landcover',
    inputProperties: bands
  });
  var classified = image.select(bands).classify(classifier);
  return classified;
};
