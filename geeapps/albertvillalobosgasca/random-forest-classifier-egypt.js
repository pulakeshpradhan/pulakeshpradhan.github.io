var point = ee.Geometry.Point([32.23264, 30.48536]);
//Search for the dataset in this case Copernicus and make some corrections
//we specify that we only want the images where our center poin is at
//filter dates, and the lower percentage of clouds possible
var generalImage = ee.ImageCollection('COPERNICUS/S2_SR')
.filterBounds(point)
.filterDate('2017-04-20', '2017-09-29')
.sort('CLOUDY_PIXEL_PERCENTAGE')
.first();
var landsatImage = ee.ImageCollection('LANDSAT/LC08/C02/T1_L2')
.filterBounds(point)
.filterDate('2013-05-01', '2013-06-01');
function applyScaleFactors(image) {
  var opticalBands = image.select('SR_B.').multiply(0.0000275).add(-0.2);
  var thermalBands = image.select('ST_B.*').multiply(0.00341802).add(149.0);
  return image.addBands(opticalBands, null, true)
              .addBands(thermalBands, null, true);
}
var dataset = landsatImage.map(applyScaleFactors);
//We made a first selection and we clip it from the image in order to
//clear some possible errors in the classification
var image = generalImage.clip(boundaries);
//As we think that the NDVI can be clarifying we calculate it
var nd = image.normalizedDifference(['B8','B4']);
//And we added as a new Band to the image
var NDVIS =image.addBands(nd);
//We can print in the console at the right some of the parameters of the image
//print(NDVIS);
// Once we selected the polygons from the image we create a new featureCollection
//that containes all the different samples and attibutes with their properties, in this case
//as a property we have the class(1,2,3,4,5)
var samples = eucaliptus.merge(cupressus_sv).merge(pinus).merge(khaya).merge(sisal).merge(other);
//We start the training, first the image Name (NDVIS),samples, the name of the property
//and the pixel precision
var training = NDVIS.sampleRegions(samples,['class'],10);
//print(training.size());
//We can specify wich band are used to do the algorithm
var bands = ['B2','B3','B4','B5','B8','nd'];
//Starts the algorithm itsef 
var clasificacion = ee.Classifier.smileRandomForest(200).train(training,'class',bands);
//We save the result as a classified image
var imageClass = NDVIS.classify(clasificacion);
//print(imageClass)
