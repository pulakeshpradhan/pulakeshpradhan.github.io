//Area calculation by class
var areaImage = ee.Image.pixelArea().addBands(
      imageClass)
var areas = areaImage.reduceRegion({
      reducer: ee.Reducer.sum().group({
      groupField: 1,
      groupName: 'class',
    }),
    geometry: boundaries,
    scale: 15,
    maxPixels: 1e10
    }); 
var classAreas = ee.List(areas.get('groups'))
var classAreaLists = classAreas.map(function(item) {
  var areaDict = ee.Dictionary(item)
  var classNumber = ee.Number(areaDict.get('class')).format()
  var area = ee.Number(
    areaDict.get('sum')).divide(1e6)
  return ee.List([classNumber, area])
})
var result = ee.Dictionary(classAreaLists.flatten())
var eucArea = ee.Number(result.get('1')).multiply(ee.Number(100))
var cupArea = ee.Number(result.get('2')).multiply(ee.Number(100))
var pinusArea = ee.Number(result.get('3')).multiply(ee.Number(100))
var khayaArea = ee.Number(result.get('5')).multiply(ee.Number(100))
var sisalArea = ee.Number(result.get('6')).multiply(ee.Number(100))
var otherArea = ee.Number(result.get('4')).multiply(ee.Number(100))
var forestArea = eucArea.add(cupArea).add(pinusArea).add(khayaArea).add(sisalArea)
