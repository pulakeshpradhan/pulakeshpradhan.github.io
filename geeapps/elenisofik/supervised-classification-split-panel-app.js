//Function to Mask landsat 8 + 9 with qa pixel band
//Create Cloud, Shadow, Snow, Water,Cirrus Mask
function maskL8srClouds(image) {
  // Bits for the masks accordingly to dataset properties.
  var dilatedcloudBitMask = (1<<1);
  var cirrusBitMask = (1<<2);
  var cloudBitMask = (1 << 3);
  var cloudShadowBitMask = (1 << 4);
  var snowBitMask = (1 << 5);
  //var waterBitMask = (1 << 7);//No water mask
  // Get the pixel QA band.
  var qa = image.select('QA_PIXEL');
  // Flags should be set to zero, indicating clear conditions.
  var mask = qa.bitwiseAnd(dilatedcloudBitMask).eq(0)
                .and(qa.bitwiseAnd(cirrusBitMask).eq(0))
                .and(qa.bitwiseAnd(cloudBitMask).eq(0))
                .and(qa.bitwiseAnd(cloudShadowBitMask).eq(0))
                .and(qa.bitwiseAnd(snowBitMask).eq(0));
  return image.updateMask(mask);
}
// Applies scaling factors.
function applyScaleFactors(image) {
  var opticalBands = image.select('SR_B.').multiply(0.0000275).add(-0.2);
  return image.addBands(opticalBands, null, true);
}
