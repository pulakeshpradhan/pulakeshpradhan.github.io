function reclass_img(image,list_value){
  var list_image = [];
  list_image[0] = image.updateMask(image.lt(list_value[0])).where(image,1);
  for(var i=0;i<list_value.length;i++){
    var y1 = image.updateMask(image.gte(list_value[i]));
    list_image[i+1] = y1.where(y1,2+i);
  }
  return [ee.ImageCollection(list_image).mosaic(),list_image];
}
