Map.addLayer(empty.paint(flowpaths, 0, 2), {'palette': "blue"}, 'Flow path');
Map.addLayer(flow_from, {'color': "red"}, 'Water outlet');
Map.addLayer(flow_to, {'color': "green"}, 'Water inlet');
