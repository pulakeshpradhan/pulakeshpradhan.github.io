var maskL8 = function(image) {
  var qa = image.select('BQA');
  var mask = qa.bitwiseAnd(1 << 4).eq(0);
  return image.updateMask(mask);
}
var composite = ee.ImageCollection('LANDSAT/LC08/C01/T1_TOA')
        .filterDate('2020-01-01', '2021-12-31')
         .map(maskL8)
          .median()
          .clip(geometry);
