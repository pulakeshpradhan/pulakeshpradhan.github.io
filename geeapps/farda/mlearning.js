Map.centerObject(newfc,11);
var bands = ['B2', 'B3', 'B4', 'B5', 'B6', 'B7'];
var training = image.select(bands).sampleRegions({
  collection: newfc, 
  properties: ['landcover'], 
  scale: 30
});
// Print(training)
// Klasifikasi citra menggunakan algoritma CART
var classifier = ee.Classifier.smileCart().train({
  features: training, 
  classProperty: 'landcover', 
  inputProperties: bands
});
print(classifier.explain());
var classified = image.select(bands).classify(classifier);
Map.addLayer(classified, {min: 0, max: 2, palette: ['red', 'green', 'blue']}, 'CART');
// Hasil CHART menggunakan batas dari "region":
var options = {
  lineWidth: 1,
  pointSize: 2,
  hAxis: {title: 'Classes'},
  vAxis: {title: 'Area m^2'},
  title: 'Area by class',
  series: {
    0: { color: 'red'},
    1: { color: 'green'},
    2: { color: 'blue'}
  }
};
var areaChart = ui.Chart.image.byClass({
  image: ee.Image.pixelArea().addBands(classified),
  classBand: 'classification', 
  region: region,
  scale: 30,
  reducer: ee.Reducer.sum()
}).setOptions(options)
  .setSeriesNames(['urban', 'vegetation', 'water']);
print(areaChart);
// CLASSIFIERS:
// Klasifikasi citra menggunakan algoritma Random Forest
var classifier = ee.Classifier.smileRandomForest(10).train({
  features: training, 
  classProperty: 'landcover', 
  inputProperties: bands,
});
// Klasifikasi citra menggunakan algoritma Naive Bayes
var classifier2 = ee.Classifier.smileNaiveBayes().train({
  features: training, 
  classProperty: 'landcover', 
  inputProperties: bands,
});
// Klasifikasi citra menggunakan algoritma GMO Maximum Entropy
var classifier3 = ee.Classifier.libsvm().train({
  features: training, 
  classProperty: 'landcover', 
  inputProperties: bands,
});
// Menggabungkan hasil 3 algoritma klasifikasi
var classified1 = image.classify(classifier);
var classified2 = image.classify(classifier2);
var classified3 = image.classify(classifier3);
// Menampilkan hasil klasifikasi menggunakan algoritma Random Forest
Map.addLayer(classified1, {min: 0, max: 2, palette: ['red', 'green', 'blue']}, 'Random Forest');
// Menampilkan hasil 3 algoritma klasifikasi menggunakan Reducer
var mode = classified1.addBands(classified2).addBands(classified3)
    .reduce(ee.Reducer.mode());
Map.addLayer(mode, {min: 0, max: 2, palette: ['red', 'green', 'blue']}, 'MODE');